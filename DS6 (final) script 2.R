# log2 transformation
qx <- as.numeric(quantile(data_sel, c(0., 0.25, 0.5, 0.75, 0.99, 1.0), na.rm=T))
LogC <- (qx[5] > 100) ||
  (qx[6]-qx[1] > 50 && qx[2] > 0)
if (LogC) { data_sel[which(data_sel <= 0)] <- NaN
exprs(data_sel) <- log2(data_sel) }
