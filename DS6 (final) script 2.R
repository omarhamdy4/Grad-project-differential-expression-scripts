summary(dT)
tT <- topTable(fit2, adjust.method="fdr", sort.by="B", p.value = 0.05, lfc = 1, number = 1000)
tT$label <- ifelse(tT$logFC >= 1 & tT$adj.P.Val <= 0.05, "Up", 
                   ifelse(tT$logFC <= -1 & tT$adj.P.Val <= 0.05, "Down", "Not significant"))

#save tT
